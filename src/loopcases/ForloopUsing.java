package loopcases;

public class ForloopUsing {

	public static void main(String[] args) {
		// intialize        //condition       //updatation   
        //4	                 //a<=40             //a=a+4  
		for(int a=4; a<=40; a=a+4) 
		{
			System.out.println(a);
			
		}
        	 for (int c=50; c>=5; c=c-5) {
        		 System.out.println(c);
        	 }
        	 for ( int p=1; p<=10; p++) {
        		 System.out.println("Mandsaur");
        	 }
        	 for (int f=9; f<=90; f=f+9) {
        		 System.out.println(f);
        	 }
        	 for(int d=100; d>=10; d=d-10) {
        		 System.out.print(d);
        	 }
        	 for(int i=10; i>=0; i--) {
        		 System.out.println(i);
        	 }
        	 for(int f=20; f<=200; f=f+20)
        	 {
        		 System.out.println();
        	 }
         
         }
		
		
		

	}


