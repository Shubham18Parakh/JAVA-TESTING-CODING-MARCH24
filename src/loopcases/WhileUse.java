package loopcases;

public class WhileUse {

	public static void main(String[] args) {
		
		  int g=90;
    	  while (g>=9) //condition part
    	  {
    		  System.out.println("I have printed 9 table in reverse order ");
    		  System.out.println(g);
    		 
    		  g=g-9; //updation part 
      }
    	  int j=9;
    	  while(j<=90) {
    		  
    		  System.out.print(j);
    		  System.out.println("I have printed 9 table in same order");
    		  j=j+9;
    	  }
    	  
 
    	  
    	
    	  }
    	  
      }

	


