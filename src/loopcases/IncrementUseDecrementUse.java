package loopcases;

public class IncrementUseDecrementUse {

	public static void main(String[] args) {

		
		int t=20;
		int k;
		k=++t;//pre increment//++x--->pre,if x is a variale
		System.out.println(t);
		System.out.println(k);
		int m=15;
		int n;
		n=--m;//pre decrement//--x/-->pre,if x is a variable
		System.out.println(m);
		System.out.println(n);
		int l=70;
		int g;
		g=l++;//post increment//x++-->post,if x is a variable
		System.out.println(l);
		System.out.println(g);
		  int p=10;
		  int q;		
		 q=p--;//post decrement//x-->post,if x is a variable
		 System.out.println(q);
		 System.out.println(p);
		
		
		
		
	}

}
